interface IVideo {
  categoryId: number;
  channelTitle: string;
  comment: number;
  description: string;
  id: string;
  like: number;
  nextToken: string;
  tag: string[];
  thumbnail: string;
  time: string;
  title: string;
  view: number;
}

interface ISearchItem {
  id: string;
  banner: string;
  name: string;
  description: string;
  subscriber: number;
  video: number;
  thumbnail: string;
  time: string;
  view: number;
}

interface minsimKeywordData {
  text: string;
  value: number;
}

const ChannelDetailPage: NextPage = () => {
  const router = useRouter();
  const query = router.query;
  const [resData, setResData] = useState('');
  
  const [chData, setChData] = useRecoilState<ISearchItem>(aChData);
  const {data: videos, status} = useQuery<IVideo[]>(["video", query.channel_id],() => {return apiIniVideoList(query.channel_id);});
  const {data: channelMinsimData, status: minsimStatus} = useQuery(["channelMinsim", query.channel_id], async ()=>{return await apiChannelMinsim(query.channel_id)})  

  useEffect(() => {
    if (channelMinsimData !== '갱신 중' && channelMinsimData !== undefined) {      
      const tmp = [...channelMinsimData.keywords]
      setResData(tmp.sort((a: minsimKeywordData, b: minsimKeywordData) => {return b.value - a.value}).slice(0, 3).map((el: minsimKeywordData) => {
        return `#${el.text}`
      }).join('  '))
    }
  }, [chData, channelMinsimData])
  
  if (status === "loading"){
    return <p>로딩중</p>
  }

  return (
    <div>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <NavBar />
        <section>
          <Banner src={chData.banner} alt="배너" />
          <ChannelInfoContainer>
            <ChannelInfoContainerInnerWrapper>
              <ChannelInfoImgTextWrapper>
                <ImgDiv>
                  <Image
                    src={chData.thumbnail}
                    alt="채널 대표 이미지"
                    layout="fill"
                    objectFit="cover"
                    style={{ borderRadius: "50%" }}
                  />
                </ImgDiv>
                <ChannelInfo
                  title={chData?.name}
                  subscriber={chData?.subscriber}
                  video={chData?.video}
                  description={chData?.description}
                ></ChannelInfo>
              </ChannelInfoImgTextWrapper>
              <Tags />
            </ChannelInfoContainerInnerWrapper>
          </ChannelInfoContainer>

          <ChannelMinsimText
            title="채널 민심"
            mainText={channelMinsimData ? `${channelMinsimData.ms}% ${channelMinsimData.ms >=50 ? '떡상' : '떡락'}` : ''}
          ></ChannelMinsimText>
          <ChannelMinsimText
            title="채널 키워드"
            mainText={channelMinsimData ? `${resData}` : ''}
          ></ChannelMinsimText>
        </section>
        <section>
          <VideoListTitle>채널 영상</VideoListTitle>

          {/* 라우터 푸시로 필요한 것 - 채널 아이디랑 채널 이름  */}
          <VideoListContainer>
            {videos ? <VideoList videos={videos} /> : <></>}
          </VideoListContainer>
        </section>
      </main>
    </div>
  );
};

export default ChannelDetailPage;

